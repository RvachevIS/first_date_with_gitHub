def plus(a, b):
    return a+b

def minus(a, b):
    return a-b

def vir(a, b):
    return 2*(a+b)

def abcd(a, b):
    return 2*(a-b)

def mul(a, b):
    return a*b


if __name__ == '__main__':
    print(plus(2, 2))
    print(minus(4, 2))
    print(mul(2, 4))
    print(vir(3, 2))
    print(abcd(3, 1))